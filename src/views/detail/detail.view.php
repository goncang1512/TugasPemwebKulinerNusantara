        <div class="section instructions">
            <h2>Cara Membuat</h2>
            <ol>
                <li>Buat biangnya terlebih dahulu. Campurkan semua bahan biang, diamkan selama 15 menit. Sisihkan.</li>
                <li>Masak santan bersama daun jeruk sampai mendidih, dinginkan. Setelah dingin, campur dengan tepung sagu.</li>
                <li>Kocok telur bersama gula pasir dengan speed rendah sampai gula larut.</li>
                <li>Setelah 15 menit, campurkan biang dengan campuran tepung sagu. Masukkan campuran tersebut ke kocokan telur dan kunyit bubuk. Tambahkan margarin, aduk rata. Diamkan adonan selama tiga jam.</li>
                <li>Tuang adonan yang telah didiamkan ke dalam cetakan ukuran 20×10×7 cm yang sudah dioles minyak dan dalam kondisi panas.</li>
                <li>Panggang di atas pasir yang sudah dipanaskan sampai gelembung berhenti dan berlubang-lubang.</li>
                <li>Lanjutkan memanggang bika ambon di oven dengan api atas suhu 190°C dengan pintu oven terbuka.</li>
            </ol>
